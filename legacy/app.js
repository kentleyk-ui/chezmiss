const APPEARANCE_STORAGE_KEY = "chez-miss-appearance-mode";

function getNestedValue(dictionary, path) {
  return path.split(".").reduce((current, segment) => current?.[segment], dictionary);
}

function getLocale() {
  const language = (navigator.language || navigator.userLanguage || "fr").toLowerCase();

  if (language.startsWith("es")) {
    return "es";
  }

  if (language.startsWith("en")) {
    return "en";
  }

  return "fr";
}

function applyTranslations(locale) {
  const dictionary = dictionaries[locale] || dictionaries.fr;

  document.documentElement.lang = locale;
  document.title = dictionary.pageTitle;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = getNestedValue(dictionary, key);

    if (typeof value === "string") {
      element.textContent = value;
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    const key = element.dataset.i18nHtml;
    const value = getNestedValue(dictionary, key);

    if (typeof value === "string") {
      element.innerHTML = value;
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.dataset.i18nPlaceholder;
    const value = getNestedValue(dictionary, key);

    if (typeof value === "string") {
      element.setAttribute("placeholder", value);
    }
  });
}

function applyCadPricing(locale) {
  const formatter = new Intl.NumberFormat(locale, {
    style: "currency",
    currency: "CAD",
    currencyDisplay: "symbol",
    minimumFractionDigits: 2,
    maximumFractionDigits: 2
  });

  document.querySelectorAll("[data-price]").forEach((element) => {
    const amount = Number.parseFloat(element.dataset.price || "0");
    element.textContent = formatter.format(amount);
  });
}

function initializeTheme() {
  document.documentElement.dataset.theme = "chez-miss";
  document.body.dataset.theme = "chez-miss";
  document.body.dataset.appearance = getSavedAppearanceMode();
  document.body.dataset.style = "hyper-glam";

  const shell = document.querySelector(".shell-theme");
  if (shell && !shell.querySelector(".glam-particles")) {
    const particles = document.createElement("div");
    particles.className = "glam-particles";
    particles.setAttribute("aria-hidden", "true");
    shell.prepend(particles);
  }
}

function getSavedAppearanceMode() {
  const saved = window.localStorage.getItem(APPEARANCE_STORAGE_KEY);
  return saved === "light" ? "light" : "dark";
}

function setAppearanceMode(appearance) {
  document.body.dataset.appearance = appearance;
  window.localStorage.setItem(APPEARANCE_STORAGE_KEY, appearance);
}

function initializeHeaderGlowOnScroll(reducedMotion) {
  const header = document.querySelector(".header");

  if (!header) {
    return;
  }

  const updateProgress = () => {
    const scrollRange = Math.max(document.documentElement.scrollHeight - window.innerHeight, 1);
    const progress = Math.min(window.scrollY / scrollRange, 1);
    header.style.setProperty("--scroll-progress", progress.toFixed(3));
  };

  updateProgress();

  if (reducedMotion) {
    return;
  }

  let rafId = 0;
  window.addEventListener("scroll", () => {
    if (rafId) {
      cancelAnimationFrame(rafId);
    }

    rafId = window.requestAnimationFrame(updateProgress);
  }, { passive: true });
}

function initializeAppearanceToggle(locale) {
  const dictionary = dictionaries[locale] || dictionaries.fr;
  const button = document.querySelector("[data-appearance-toggle]");

  if (!button) {
    return;
  }

  const render = () => {
    const isLight = document.body.dataset.appearance === "light";
    button.setAttribute("aria-pressed", String(isLight));

    const nextLabel = locale === "fr"
      ? (isLight ? "Activer le mode sombre" : "Activer le mode clair")
      : locale === "es"
        ? (isLight ? "Activar modo oscuro" : "Activar modo claro")
        : (isLight ? "Enable dark mode" : "Enable light mode");

    button.setAttribute("aria-label", nextLabel);
    button.setAttribute("title", nextLabel);
  };

  button.addEventListener("click", () => {
    const nextAppearance = document.body.dataset.appearance === "light" ? "dark" : "light";
    setAppearanceMode(nextAppearance);
    render();
  });

  render();
}

function initializeSignatureLoader(locale) {
  const dictionary = dictionaries[locale] || dictionaries.fr;
  const loader = document.createElement("div");
  loader.className = "signature-loader";
  loader.innerHTML = `
    <div class="signature-loader-mark">
      <div class="signature-loader-title">CHEZ <span>MISS</span></div>
      <div class="signature-loader-sub">${dictionary.brandTagline}</div>
    </div>
  `;

  document.body.append(loader);

  requestAnimationFrame(() => {
    loader.classList.add("is-active");
  });

  window.setTimeout(() => {
    loader.classList.add("is-exit");
  }, 980);

  window.setTimeout(() => {
    loader.remove();
  }, 1620);
}

function initializeParallax(reducedMotion) {
  if (reducedMotion) {
    return;
  }

  const surfaces = Array.from(
    document.querySelectorAll(".header, .hero-products, .values, .section, .elegance-visual, .footer, .product-card-main")
  );

  surfaces.forEach((surface) => {
    surface.classList.add("parallax-surface");
  });

  let rafId = 0;

  const updateParallax = (xPercent, yPercent) => {
    surfaces.forEach((surface) => {
      const isMajorSurface = surface.matches(".hero-products, .section, .elegance-visual, .footer");
      const depth = isMajorSurface ? 14 : 7;
      const tx = xPercent * depth;
      const ty = yPercent * depth;
      const rx = yPercent * -4.2;
      const ry = xPercent * 5.8;
      surface.style.transform = `translate3d(${tx}px, ${ty}px, 0) rotateX(${rx}deg) rotateY(${ry}deg)`;
    });
  };

  document.addEventListener("pointermove", (event) => {
    const x = event.clientX / window.innerWidth - 0.5;
    const y = event.clientY / window.innerHeight - 0.5;

    if (rafId) {
      cancelAnimationFrame(rafId);
    }

    rafId = requestAnimationFrame(() => {
      updateParallax(x, y);
    });
  });

  document.addEventListener("pointerleave", () => {
    surfaces.forEach((surface) => {
      surface.style.transform = "translate3d(0, 0, 0) rotateX(0deg) rotateY(0deg)";
    });
  });
}

function initializeMotion() {
  const reducedMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

  if (!reducedMotion) {
    document.addEventListener("pointermove", (event) => {
      const x = (event.clientX / window.innerWidth) * 100;
      const y = (event.clientY / window.innerHeight) * 100;
      document.body.style.setProperty("--cursor-x", `${x}%`);
      document.body.style.setProperty("--cursor-y", `${y}%`);
    });
  }

  const revealTargets = document.querySelectorAll(
    ".hero-text, .hero-products, .value-item, .section-header, .product-card-main, .elegance-text, .elegance-visual, .footer-brand, .footer-newsletter, .footer-links, .footer-bottom"
  );

  const cinematicTargets = document.querySelectorAll(".values, .section, .elegance-visual, .footer");

  cinematicTargets.forEach((element) => {
    element.classList.add("cinematic-reveal");
  });

  initializeHeaderGlowOnScroll(reducedMotion);
  initializeParallax(reducedMotion);

  revealTargets.forEach((element, index) => {
    element.classList.add("reveal-on-scroll");
    element.style.setProperty("--reveal-delay", `${Math.min(index * 55, 560)}ms`);
  });

  if (reducedMotion || !("IntersectionObserver" in window)) {
    revealTargets.forEach((element) => {
      element.classList.add("is-visible");
    });
    cinematicTargets.forEach((element) => {
      element.classList.add("is-visible");
    });
    return;
  }

  document.body.classList.add("motion-ready");

  const observer = new IntersectionObserver(
    (entries, instance) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }

        entry.target.classList.add("is-visible");
        instance.unobserve(entry.target);
      });
    },
    {
      rootMargin: "0px 0px -10% 0px",
      threshold: 0.16
    }
  );

  revealTargets.forEach((element) => {
    observer.observe(element);
  });

  cinematicTargets.forEach((element) => {
    observer.observe(element);
  });

  // Safety net to prevent invisible sections if observer callbacks are delayed.
  window.setTimeout(() => {
    revealTargets.forEach((element) => {
      element.classList.add("is-visible");
    });

    cinematicTargets.forEach((element) => {
      element.classList.add("is-visible");
    });
  }, 1200);
}

function bootChezMiss() {
  const locale = getLocale();
  initializeTheme();
  applyTranslations(locale);
  applyCadPricing(locale);
  initializeAppearanceToggle(locale);
  initializeSignatureLoader(locale);
  initializeMotion();
}

document.addEventListener("DOMContentLoaded", bootChezMiss);